// ─── i18n Manager ─────────────────────────────────────────────────────────────
let _currentLang: LangCode = 'en';
const _listeners: Array<() => void> = [];

export const i18n = {
  get lang(): LangCode {
    return _currentLang;
  },

  setLang(lang: LangCode) {
    _currentLang = lang;
    if (typeof localStorage !== 'undefined') localStorage.setItem('nexus_lang', lang);
    _listeners.forEach((fn) => fn());
  },

  init() {
    if (typeof localStorage !== 'undefined') {
      const saved = localStorage.getItem('nexus_lang') as LangCode | null;
      if (saved && TRANSLATIONS[saved]) _currentLang = saved;
    }
  },

  t(key: string): string {
    return TRANSLATIONS[_currentLang]?.[key] ?? TRANSLATIONS.en?.[key] ?? key;
  },

  /** Subscribe to language change events (for React state updates) */
  subscribe(fn: () => void): () => void {
    _listeners.push(fn);
    return () => {
      const idx = _listeners.indexOf(fn);
      if (idx !== -1) _listeners.splice(idx, 1);
    };
  },

  allLangs: Object.entries(LANG_NAMES) as [LangCode, string][],
};
